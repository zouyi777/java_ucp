package com.zyyu.ucp.model.vo;

public class TopCardVo {
    private int userCount;
    private int dailyActiveCount;
    private int worksCount;
    private int noticeCount;

    public int getUserCount() {
        return userCount;
    }

    public void setUserCount(int userCount) {
        this.userCount = userCount;
    }

    public int getDailyActiveCount() {
        return dailyActiveCount;
    }

    public void setDailyActiveCount(int dailyActiveCount) {
        this.dailyActiveCount = dailyActiveCount;
    }

    public int getWorksCount() {
        return worksCount;
    }

    public void setWorksCount(int worksCount) {
        this.worksCount = worksCount;
    }

    public int getNoticeCount() {
        return noticeCount;
    }

    public void setNoticeCount(int noticeCount) {
        this.noticeCount = noticeCount;
    }
}
